public class PrimeiroPrograma {  //classe
    public static void main(String[] args) { //método (main), "psvm" escreve todo o comando do método
       System.out.println("Ola, Mundo!"); //saída na tela, "sout" escreve todo o comando de saída 
    }                                       //"println" imprime a mensagem e pula uma linha 
}


/*Ou seja, tem um método principal (main) dentro de uma classe (PrimeiroPrograma) que faz parte do 
pacote (primeiroprograma).
Classes são compostas por métodos, e um pacote pode ter várias classes*/

